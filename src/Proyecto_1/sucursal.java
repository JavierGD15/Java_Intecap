package Proyecto_1;

public class sucursal {

	int codigo;
	String nombre;
	String direccion;
	String correo;
	int telefono;
	
	public sucursal(int codigo, String nombre, String direccion, String correo, int telefono) {
		
		this.codigo = codigo;
		this.nombre = nombre;
		this.direccion = direccion;
		this.correo = correo;
		this.telefono = telefono;
	}
	
	
}
